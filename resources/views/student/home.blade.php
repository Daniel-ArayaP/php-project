                    <p>
                        El trabajo final de graduación (TFG) es un ejercicio académico que el estudiantado de la carrera de la Escuela de Ingeniería en Sistemas Computacionales debe realizar para optar por el grado de Licenciatura.
                        El estudiante debe haber concluido todos los cursos del nivel de Licenciatura establecidos en la carrera que usted cursa y debe contar con la debida autorización por parte de la persona encargada de la carrera.
                        La Tesis como modalidad de TFG, es el proceso de investigación que le permite al estudiante desarrollar estudios con metodología científica, enmarcados dentro de alguno de los enfoques de investigación vigentes, así como desarrollar el pensamiento crítico y reflexivo en situaciones problemáticas que demandan la aplicación de la rigurosidad metodológica, al planificar y desarrollar la investigación.
                        La modalidad Proyecto de Graduación tiene como objetivo, permitir a uno o dos estudiantes, previa autorización de la Comisión de TFG, realizar un trabajo especializado en su rama de estudio, aplicado a un problema específico. Este proyecto puede ser de investigación, creación, asesoría u orientación y debe constituir, un aporte al mejoramiento del objeto de estudio.
                        Es un proceso dirigido al diagnóstico de las características o magnitud del problema objeto de investigación, así como la propuesta de una solución viable. Se trata de encontrar y proponer soluciones a problemas concretos.
                        Es posible, como proyecto de graduación, poder complementar o poner en práctica sugerencias planteadas en investigaciones anteriores, pero en nuevos contextos, y de esta manera demostrar los conocimientos y la creatividad del o los estudiantes, para que por medio de sus proposiciones formulen respuestas concretas a los problemas identificados.
